// You can add interactivity using JavaScript.
// For example, you can create an image slideshow or interactive messages.

// Example: Change the header background when the user hovers over it
const header = document.querySelector('header');
header.addEventListener('mouseover', function () {
    header.style.background = 'linear-gradient(rgba(255, 99, 71, 0.8), rgba(255, 140, 102, 0.8)), url("header-bg2.jpg")';
});
